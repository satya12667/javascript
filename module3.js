export const pi=3.456989;
export function circle(radius){
  return 2 *pi * radius;
}
export function square(radius){
  return 5 *pi * radius;
}export function rectangle(radius){
  return 8 *pi * radius;
}export function cube(radius){
  return 6 *pi * radius;
}export function oval(radius){
  return 3 *pi * radius;
}